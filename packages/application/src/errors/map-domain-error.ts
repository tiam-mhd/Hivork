export function mapDomainError(error: unknown): ApplicationError {
  if (error instanceof DomainError) {
    return new ApplicationError(
      error.code,
      error.message,
      DOMAIN_ERROR_HTTP[error.code] ?? 400,
    );
  }

  throw error;
}
